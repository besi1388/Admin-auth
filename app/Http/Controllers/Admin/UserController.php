    public function status (Request $request, $id ){
        $users = User::all();
        $user = $users->find($id);

        if ($user->status == 0){
            $user->status = 1;
        }else{
            $user->status = 0;
        }

        $user->save();
        return view('admin.users.index', compact('users'))->with('success','Status has been changed successfully.');
    }
